#include <unistd.h>

int		ft_strcmp(char *s1, char *s2);
void	printer(int argc, char **argv);

int	main(int argc, char **argv)
{
	char	*gato;
	int		i;
	int		j;

	i = 1;
	while (i < argc)
	{
		j = i + 1;
		while (j < argc)
		{
			if (ft_strcmp(argv[i], argv[j]) > 0)
			{
				gato = argv[i];
				argv[i] = argv[j];
				argv[j] = gato;
			}
			j++;
		}
		i++;
	}
	printer(argc, argv);
	return (0);
}

void	printer(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}

int	ft_strcmp(char *s1, char *s2)
{	
	int		i;

	i = 0;
	while ((s1[i] != '\0') && (s2[i] != '\0') && (s1[i] == s2[i]))
	{
		i++;
	}
	return (s1[i] - s2[i]);
}
